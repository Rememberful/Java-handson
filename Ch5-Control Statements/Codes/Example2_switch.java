public class Example2_switch {
    public static void main(String[] args) {
        int a = 10;
        switch(a){
            case 2:
                System.out.println("2");
            case 5:
                System.out.println("5");
            case 10:
                System.out.println("10");
            default:
                System.out.println("Default");
        }
    }
}
