public class ModulusOperator {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        int c = a % b;
        System.out.println("a % b: " + c);

        float f1 = 24.09f;
        float f2 = 3f;
        float f3 = f1%f2;
        System.out.println("f1 % f2: " + f3);
    }
}
