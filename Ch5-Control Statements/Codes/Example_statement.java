public class Example_statement {
    public static void main(String[] args) {
        int a = 10;
        switch(a){
            case 2:
                System.out.println("2");
                break;
            case 5:
                System.out.println("5");
                break;
            case 10:
                System.out.println("10");
                break;
            default:
                System.out.println("Default");
        }
    }
}
